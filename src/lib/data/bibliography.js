// Get bibliography entry by source string
export function getBibEntry(sourceKey) {
  return bibliography[sourceKey] || null;
}

// Get all entries sorted by author/year
export function getAllBibEntries() {
  return Object.entries(bibliography)
    .map(([key, entry]) => ({ ...entry, key }))
    .sort((a, b) => {
      // Sort by year (descending), then by author
      if (a.year !== b.year) {
        return (b.year || 0) - (a.year || 0);
      }
      return (a.authors || '').localeCompare(b.authors || '');
    });
}
